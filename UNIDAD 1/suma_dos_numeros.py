#Cree una funcion que permita realizar la suma de dos numeros
def sumarDosNumeros():
    num1 = float(input("Ingrese el primer numero: "))
    num2 = float(input("Ingrese el segundo numero :" ))
    print ("La suma de ", int(num1), " + ", int(num2), " es igual a: ", int(num1)+int(num2),"\n")
    
sumarDosNumeros()

